#include <iostream>
#include <stdio.h>
#include <math.h>

int main()
{
    // initializing a float value
    float num = 3.14;

    // returning the floor value of a float value
    int result = floor(num);

    // printing the floor value
    printf("Floor integer of %.2f = %d", num, result);

    return 0;
}
